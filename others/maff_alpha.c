#include <unistd.h>

int	main(void)
{
	char	c;
	int	i;

	c = 'a';
	i = 1;
	while (i <= 26)
	{
		if (i % 2 == 0)
			c -= 32;
		write(1, &c, 1);
		if (c >= 'A' && c <= 'Z')
			c += 32;
		i++;
		c++;
	}
	write(1, "\n", 1);
	return (0);
}
